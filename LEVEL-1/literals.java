
class literals {

    public static void main(String[] args) {
        //literals
        int n = 0b101;
        int a = 0x7E;
        int i = 10_00_00_000;
        int t = 100000000;
        char c = 'a';
        c++;
        System.err.println(c);
        System.err.println(t);
        System.err.println(i);
        System.err.println(n);
        System.err.println(a);
    }
}
